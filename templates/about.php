<h2>Unser Team</h2>
<h3><span style="color:#4A4A4A"><em>Paartherapie</em></span></h3>

<p>Die <strong>Psychodiagnostische Beratungspraxis</strong> arbeitet seit 1990 auf dem Gebiet der Paarberatung und Paartherapie. </p>
<p>Als <strong>ausgebildete Paartherapeuten</strong> haben wir uns mit allen bedeutsamen Methoden der Paatherapie auseinandergesetzt. Bei einigen dachten wir: Das ist nur was für Leute aus dem psychosozialen Bereich. 
<strong>Das geht auch besser!</strong> Wir wollten zusätzliche Methoden entwickeln, die für fast alle Paare funktionieren.</p>
