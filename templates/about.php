<h3><span style="color:#4A4A4A"><em>"Bodenständigere" Ansätze der Paartherapie</em></span></h3>

<p>Aus diesen Überlegungen heraus entstanden ab 2011 eine Reihe <strong>neuer paartherapeutischer Ansätze</strong>, welche bodenständiger daherkommen als viele etablierte Methoden.  <br>
Wir haben diese Ansätze mittlerweile in den Kanon bereits bestehender Methoden gut integriert. Und daraus eine umfassende 
<strong>Methodenausbildung der Paartherapie</strong> entwickelt.
Auf diese Weise wollen wir unser Wissen und unsere Erfahrung an möglichst viele, die in diesem Bereich arbeiten, weitergeben.<br>
Doch es ging noch weiter. Eine der von uns entwickelten Übungen heißt: <strong>"Wünschen und Helfen"</strong>. Sie beinhaltet, erstmal jede/r für sich Wünsche an den Partner zu formulieren. Und dann zunächst getrennt voneinander zu schauen: Wie könnte der Partner/die Partnerin bei der Verwirklichung helfen? Um dann in Austausch zu gehen, ob beide Partner etwas für die Wunschverwirklichung tun wollen. Das 
"von Vorwürfen zu Wünschen gelangen" ist dabei ein sehr wichtiges Element.</p>
<h3><span style="color:#4A4A4A"><em>Die neue Idee: Paartherapie durch Selbsttest und Tool?</em></span></h3>

<p>Darauf aufbauend hatten wir die Idee: <strong>Kann man Teile unserer Paartherapie in einen Selbsttest einbauen?</strong> Der den Klienten hilft, erstmal die eigenen Wünsche an den Partner/die Partnerin zu klären? Vielleicht, um zunächst eigenständig, ohne Paartherapie, mit dem Partner etwas zu verändern?<br>
Wir haben ein Jahr Arbeit investiert. Alle Themen gesammelt, die uns in den zahlreichen therapeutischen Sitzungen begegnet sind. Und am Ende den <strong>Hamburger Paartherapie-Test</strong> entwickelt.<br>

Wir freuen uns auf Ihr Feedback. Und vielleicht auch darauf, Sie in unseren 
Praxisräumen kennenzulernen. Falls Sie eine Paatherapie bei uns machen wollen - oder eine Ausbildung zum/zur Paartherapeuten/Paartherapeutin. <br>
