<h3><span style="color:#4A4A4A"><em>Selbsttest "Wünsche klären"</em></span></h3>
<p>Wenn in Ihrer Partnerschaft Veränderungswünsche aufkommen, dann ist es gut, erstmal 
<strong>jede/r für sich</strong> zu klären, worum es Ihnen wirklich geht. Genau das leistet dieser Selbsttest: 
Die eigenen <strong>Wünsche an den Partner/die Partnerin</strong> zu klären.<br>
Wir haben dazu aus unserer <strong>15-jährigen Erfahrung als Paartherapeuten</strong> alle geäußerten Partnerschafts-Wünsche (insgesamt 130) in den Selbsttest eingepflegt. Finden Sie im Testverlauf 
