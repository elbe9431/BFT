welcher Ideen aufzeigt, wie Sie diesen in die Partnerschaft einbringen 
könnten.</p>
<h3><span style="color:#4A4A4A"><em>Tool "Lösungen finden"</em></span></h3>
<p>Das Tool <strong>"Lösungen finden" baut auf "Wünsche klären" auf</strong>. Sie bringen dabei Ihre bedeutsamsten Wünsche in eine Rangfolge - was ist Ihnen wirklich wichtig? Was genau bewegt Sie dabei? Gedanken und Hinweise, welche wir aus unseren Paartherapien abgeleitet haben, begleiten Sie dabei.<br>
